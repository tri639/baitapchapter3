public class bai12 {
    public static void main(String[] args) {

        int rows = 10;
        int colums = 20;
        for (int i = 1; i <= rows; i++) {
            for (int n = 1; n <= colums; n++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
